/**
 * Post-build: hash every production JS bundle + service worker, write CSP allowlist.
 */
import { createHash } from 'node:crypto';
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildDocumentCsp } from '../api/_lib/cspPolicy.ts';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const distAssets = join(root, 'dist/assets');
const distSw = join(root, 'dist/sw.js');

function sha256Base64(filePath) {
  const content = readFileSync(filePath);
  return createHash('sha256').update(content).digest('base64');
}

const scriptHashes = readdirSync(distAssets)
  .filter((name) => name.endsWith('.js'))
  .map((name) => sha256Base64(join(distAssets, name)))
  .sort();

if (scriptHashes.length === 0) {
  console.error('sync-production-csp: no JS files in dist/assets — run vite build first');
  process.exit(1);
}

const workerHashes = [];
try {
  workerHashes.push(sha256Base64(distSw));
} catch {
  console.warn('sync-production-csp: dist/sw.js not found — worker-src will be none');
}

const policy = buildDocumentCsp({ scriptHashes, workerHashes });

const generatedTs = `/** Auto-generated by scripts/sync-production-csp.mjs — do not edit manually */
export const PRODUCTION_SCRIPT_HASHES: string[] = ${JSON.stringify(scriptHashes, null, 2)};

export const PRODUCTION_WORKER_HASHES: string[] = ${JSON.stringify(workerHashes, null, 2)};

export const PRODUCTION_CONTENT_SECURITY_POLICY = ${JSON.stringify(policy)};
`;

writeFileSync(join(root, 'api/_lib/csp.generated.ts'), generatedTs);

const vercelPath = join(root, 'vercel.json');
const vercel = JSON.parse(readFileSync(vercelPath, 'utf8'));
const docHeaders = vercel.headers.find((h) => h.source === '/(.*)');
const cspHeader = docHeaders?.headers.find((h) => h.key === 'Content-Security-Policy');
if (cspHeader) {
  cspHeader.value = policy;
  writeFileSync(vercelPath, `${JSON.stringify(vercel, null, 2)}\n`);
} else {
  console.warn('sync-production-csp: CSP header not found in vercel.json');
}

console.log(
  `sync-production-csp: ${scriptHashes.length} script hash(es), ${workerHashes.length} worker hash(es)`
);
